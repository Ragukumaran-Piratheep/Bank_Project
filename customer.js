class Customer {

    constructor(CustomerID, Name, PhoneNumber, Address, AccountNumber) {

        this.CustomerID = CustomerID;
        this.Name = Name;
        this.PhoneNumber = PhoneNumber;
        this.Address = Address;
        this.AccountNumber = AccountNumber;
    }
    depositeMoney() {
        return this.depositeMoney;
    }
    withdrawMoney() {
        return this.withdrawMoney;
    }
    openAccount() {
        return this.openAccount;
    }
    closeAccount() {
        return this.closeAccount;
    }
    appplyForLoan() {
        return this.appplyForLoan;
    }
}